package com.warehouse.pojo;

import java.util.ArrayList;
import java.util.List;

public class DocumentNumber {

    private String docnId;

    private int docnDateType;

    private String docnType;

    private DocumentPrefix documentPrefix;

    private List<DocumentPrefix> documentPrefixList = new ArrayList<>();

    public String getDocnId() {
        return docnId;
    }

    public void setDocnId(String docnId) {
        this.docnId = docnId;
    }

    public int getDocnDateType() {
        return docnDateType;
    }

    public void setDocnDateType(int docnDateType) {
        this.docnDateType = docnDateType;
    }

    public String getDocnType() {
        return docnType;
    }

    public void setDocnType(String docnType) {
        this.docnType = docnType;
    }

    public DocumentPrefix getDocumentPrefix() {
        return documentPrefix;
    }

    public void setDocumentPrefix(DocumentPrefix documentPrefix) {
        this.documentPrefix = documentPrefix;
    }

    public List<DocumentPrefix> getDocumentPrefixList() {
        return documentPrefixList;
    }

    public void setDocumentPrefixList(List<DocumentPrefix> documentPrefixList) {
        this.documentPrefixList = documentPrefixList;
    }
}
